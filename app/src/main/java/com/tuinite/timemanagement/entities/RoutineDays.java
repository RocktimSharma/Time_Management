package com.tuinite.timemanagement.entities;

public class RoutineDays {
    public Boolean sun, mon, tue, wed, thu, fri, sat;

    public RoutineDays(Boolean sun, Boolean mon, Boolean tue, Boolean wed, Boolean thu, Boolean fri, Boolean sat) {
        this.sun = sun;
        this.mon = mon;
        this.tue = tue;
        this.wed = wed;
        this.thu = thu;
        this.fri = fri;
        this.sat = sat;
    }
}
